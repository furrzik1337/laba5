# Считываем ввод от пользователя
month_number = int(input("Введите номер месяца (от 1 до 12): "))

# Создаем список названий месяцев
months = [
    'Январь', 'Февраль', 'Март',
    'Апрель', 'Май', 'Июнь',
    'Июль', 'Август', 'Сентябрь',
    'Октябрь', 'Ноябрь', 'Декабрь'
]

# Проверяем корректность ввода
if 1 <= month_number <= 12:
    # Выводим название месяца, учитывая, что индексация начинается с нуля
    print(months[month_number - 1])
else:
    print("Ошибка! Номер месяца должен быть от 1 до 12.")